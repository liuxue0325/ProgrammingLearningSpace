# print(price['荔枝'])

#通过循环，配合相应的信息，将字典中的水果和价格打印出来
print("今日水果价格：")
for fruit in price:
    print("%s%d元/斤"%(fruit,price[fruit]))
print("")
#输入购买水果
n=int(input('请输入购买水果的种类数量：'))

#设置sum_price存储总金额
sum_price=0

for i in range(0,n):
    #输入购买的水果名称与数量，存储至fruit与num
    fruit=input('输入购买水果%d的名称：'%(i+1))
    num=int(input('输入购的水果%d的数量(斤)：'%(i+1)))

    #如果输入的水果在price字典中
    if fruit in price:
        #将水果单价price[fruit]乘以购买的数量num累加到sum_price
        sum_price+=price[fruit]*num
print('总价格为%d'%(sum_price))#最后输入总价格sum_price
