"""
已知蔬果价格和数量，如何编写python程序计算需要支付的总金额。
预先设置出售的水果与相应的价格，苹果5元/斤，桃子6元/斤，香蕉3元/斤，梨子4元/斤;
输入整数n代表要购买的水果种类数量;
输入n行水果名称何购买数量;
程序计算总价格后输出到屏幕。
如果要购买的水果超市中没有，则忽略它不参与计算。
"""
"""
字典的概念与创建
对象于对象之间的对应关系，即映射。
    例如四种水果分别放在四个筐，则苹果在A筐 桃子在B筐 香蕉和梨子分别在C和D筐是它们的映射关系
    字典就是用来表示这种映射关系的结构
字典的语法：
    dict={key1:value1,key2:value2,...}
    dict表示字典名，key表示映射的键，value表示映射的值，映射关系之间用逗号分隔。
"""
